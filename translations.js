// Language switching functionality
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('selectedLanguage') || 'fr';
        this.init();
    }

    init() {
        // Apply saved language on page load
        this.switchLanguage(this.currentLang);
        
        // Setup language selector events
        this.setupLanguageSelector();
    }

    setupLanguageSelector() {
        const languageToggle = document.querySelector('.language-toggle');
        const languageDropdown = document.querySelector('.language-dropdown');
        const languageOptions = document.querySelectorAll('.language-option');
        const mobileLanguageOptions = document.querySelectorAll('.mobile-language-option');

        // Toggle dropdown
        if (languageToggle) {
            languageToggle.addEventListener('click', (e) => {
                e.stopPropagation();
                languageDropdown.classList.toggle('active');
            });
        }

        // Desktop language selection
        languageOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.dataset.lang;
                this.switchLanguage(lang);
                languageDropdown.classList.remove('active');
            });
        });
        
        // Mobile language selection
        mobileLanguageOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.dataset.lang;
                this.switchLanguage(lang);
                
                // Update active state for mobile buttons
                mobileLanguageOptions.forEach(opt => opt.classList.remove('active'));
                option.classList.add('active');
                
                // Close mobile menu after language change
                setTimeout(() => {
                    const mobileMenuOverlay = document.querySelector('.mobile-menu-overlay');
                    const mobileMenuToggle = document.querySelector('.mobile-menu-toggle');
                    if (mobileMenuOverlay) {
                        mobileMenuOverlay.classList.remove('active');
                    }
                    if (mobileMenuToggle) {
                        mobileMenuToggle.classList.remove('active');
                    }
                }, 300);
            });
        });

        // Close dropdown when clicking outside
        document.addEventListener('click', () => {
            if (languageDropdown) {
                languageDropdown.classList.remove('active');
            }
        });
        
        // Set initial active state for mobile buttons
        this.updateMobileLanguageButtons();
    }
    
    updateMobileLanguageButtons() {
        const mobileLanguageOptions = document.querySelectorAll('.mobile-language-option');
        mobileLanguageOptions.forEach(option => {
            if (option.dataset.lang === this.currentLang) {
                option.classList.add('active');
            } else {
                option.classList.remove('active');
            }
        });
    }

    switchLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('selectedLanguage', lang);
        
        // Update current language display
        const currentLangDisplay = document.querySelector('.current-lang');
        if (currentLangDisplay) {
            currentLangDisplay.textContent = lang.toUpperCase();
        }

        // Update page language attribute
        document.documentElement.lang = lang;

        // Translate all elements with data-translate attribute
        this.translatePage();
        
        // Update mobile buttons state
        this.updateMobileLanguageButtons();
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = translations[this.currentLang][key];
            
            if (translation) {
                // Check if element is a button or has text content
                if (element.tagName === 'BUTTON' || element.tagName === 'A') {
                    element.innerHTML = translation;
                } else {
                    element.innerHTML = translation;
                }
            }
        });

        // Update placeholder texts if any
        const placeholders = document.querySelectorAll('[data-translate-placeholder]');
        placeholders.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = translations[this.currentLang][key];
            if (translation) {
                element.placeholder = translation;
            }
        });
    }
}

// Initialize language manager when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    new LanguageManager();
});